	end
	if(CurTime() - rtTime >= 5) then
		draw.RoundedBox( 8, ScrW() / 2 + 85, 32, 5, 8, Color(255,0,0,255) ) 
	end
	if(CurTime() - rtTime >= 6) then
		draw.RoundedBox( 8, ScrW() / 2 + 105, 32, 5, 8, Color(255,0,0,255) ) 
	end
	if(CurTime() - rtTime >= 7) then
		draw.RoundedBox( 8, ScrW() / 2 + 125, 32, 5, 8, Color(255,0,0,255) ) 
