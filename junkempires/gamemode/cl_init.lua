		draw.DrawText("Gold: " .. LocalPlayer():GetNWInt(LocalPlayer():Nick() .. "_gold") .. " /1000 " .. "(+" .. LocalPlayer():GetNWInt(LocalPlayer():Nick() .. "_cities") * 1 .. ")", "ScoreboardText", 360, 5, Color(255,255,0,255),1)
	end
	
	draw.RoundedBox( 8, ScrW() / 2 - 20, 30, 200, 12, Color(0,0,0,155) ) 
	if(CurTime() - rtTime >= 1) then
		draw.RoundedBox( 8, ScrW() / 2 + 5, 32, 5, 8, Color(255,0,0,255) ) 
